$("#minit_input").on("click",function (){
var focus = document.getElementById('minit_input');
    focus.classList.toggle('active');
    setTimeout(function() {
        var focus = document.getElementById('minit_input');
        focus.classList.toggle('active');
    },200);   // input表单点击缩放事件
})
// input表单点击缩放
